void show_log_page (void)
{
	int
		loop,
		start,
		end;

	ui_object_destroy_list_items (message_list);

	start = get_message_log_number_of_messages () - 1;

	end = max (start - MAX_LOG_DISPLAY_MESSAGES, 0);

	for (loop = start; loop >= end; loop --)
	{
		add_message_to_campaign_log (loop);
	}

	display_campaign_page (CAMPAIGN_PAGE_LOG, ENTITY_INDEX_DONT_CARE, TRUE);

	resize_pop_up_list (message_list);
}

