/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void add_message_to_campaign_log (int index)
{
	int
		hours,
		minutes,
		seconds;

	char
		*s;

	message_log_type
		*message;

	rgb_colour
		*col = NULL;

	ui_object
		*new_object;

	message = get_message_log (index);

	ASSERT (message);

	get_digital_clock_int_values (message->time_of_day, &hours, &minutes, &seconds);

	s = (char *) malloc_fast_mem (strlen (message->string) + 20);

	sprintf (s, "[%02d:%02d] %s", hours, minutes, message->string);

	col = &(message->colour);

	ASSERT (col);

	new_object = add_to_pop_up_list (s, message_list, NULL, index, -1, *col);

	set_ui_object_highlightable (new_object, TRUE);

	col = &ui_ingame_highlight_text_colour;
	set_ui_object_highlighted_font_colour (new_object, col->r, col->g, col->b, col->a);

	col = &ui_ingame_selected_text_colour;
	set_ui_object_selected_font_colour (new_object, col->r, col->g, col->b, col->a);

	free_mem (s);
}

