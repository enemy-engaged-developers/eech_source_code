void overload_log_page_message_responses (void)
{
	campaign_screen_message_targets
		target;

	target = CAMPAIGN_SCREEN_TARGET_LOG_PAGE;

	campaign_screen_message_responses [target][CAMPAIGN_SCREEN_FORCE_UPDATE]	=	response_to_force_update;

	campaign_screen_message_responses [target][CAMPAIGN_SCREEN_NEW_MESSAGE]		=	response_to_new_message;
}

