static int response_to_force_update (campaign_screen_messages message, entity *sender)
{
	if (get_ui_object_drawable (campaign_page [CAMPAIGN_PAGE_LOG]))
	{
		show_log_page ();
	}

	return TRUE;
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

static int response_to_new_message (campaign_screen_messages message, entity *sender)
{
	vec3d
		*pos;

	if (sender)
	{
		pos = get_local_entity_vec3d_ptr (sender, VEC3D_TYPE_POSITION);

		if (pos)
		{
			if (point_inside_map_area (pos))
			{
				set_page_3d_viewpoint (pos);
			}
		}
	}

	if (get_ui_object_drawable (campaign_page [CAMPAIGN_PAGE_LOG]))
	{
		show_log_page ();
	}

	return TRUE;
}

