void set_page_3d_viewpoint (vec3d *pos)
{
	vec3d
		vec;

	ASSERT (pos);
	
	get_3d_unit_vector_from_heading_and_pitch (&vec, rad (180.0f), rad (45.0f));

	page_vp.x = pos->x + (page_3d_distance * vec.x);
	page_vp.y = pos->y + (page_3d_distance * vec.y);
	page_vp.z = pos->z + (page_3d_distance * vec.z);

	invert_3d_vector (&vec);

	get_matrix3x3_from_unit_vec3d (page_vp.attitude, &vec);

	page_map_dimensions.x = pos->x;
	page_map_dimensions.z = pos->z;
}

