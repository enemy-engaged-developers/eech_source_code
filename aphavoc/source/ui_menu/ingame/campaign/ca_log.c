void initialise_campaign_screen_log_page_objects (void)
{
	vec3d
		pos;
		
	ui_object_destroy_list_items (message_list);

	current_index = -1;

	page_map_dimensions.x = MID_MAP_X;
	page_map_dimensions.z = MID_MAP_Z;
	page_map_dimensions.size = 5.0 * KILOMETRE; 

	page_map_dimensions.subject_entity = NULL;
	page_map_dimensions.selected_entity = NULL;

	clear_map_mouse_over_object (&page_map_dimensions);

	pos.x = MID_MAP_X;
	pos.z = MID_MAP_Z;

	pos.y = get_3d_terrain_elevation (pos.x, pos.z);

	set_page_3d_viewpoint (&pos);
}

