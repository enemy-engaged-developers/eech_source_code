static void notify_message_list (ui_object *obj, void *arg)
{
	message_log_type
		*message;

	current_index = get_ui_object_item_number (obj);

	if (current_index >= 0)
	{
		message = get_message_log (current_index);

		ASSERT (message);

		if (point_inside_map_area (&(message->position)))
		{
			page_map_dimensions.x = message->position.x;
			page_map_dimensions.z = message->position.z;

			add_map_event (MAP_EVENT_LOG_EVENT, message->position.x, message->position.z, 1.0);

			set_page_3d_viewpoint (&(message->position));
		}
	}
}

