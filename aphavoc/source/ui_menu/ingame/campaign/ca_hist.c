#define DEBUG_MODULE 0

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#define MAX_HISTORY_ITEMS	24

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct CAMPAIGN_HISTORY_TYPE
{
	campaign_pages
		page;

	int
		index;
};

typedef struct CAMPAIGN_HISTORY_TYPE campaign_history_type;

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

static campaign_history_type
	campaign_history [MAX_HISTORY_ITEMS],
	temp [MAX_HISTORY_ITEMS];

static int
	pop_flag = 0,
	num_campaign_history_items = 0;

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void initialise_campaign_history (void)
{
	pop_flag = 0;

	num_campaign_history_items = 0;
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int get_campaign_history_valid (void)
{
	if (num_campaign_history_items > 0)
	{
		return TRUE;
	}
	else
	{
		return FALSE;
	}
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void push_campaign_history (campaign_pages page, int index)
{
	if (!pop_flag)
	{
		if (num_campaign_history_items >= MAX_HISTORY_ITEMS)
		{
			memcpy (temp, &campaign_history [1], sizeof (campaign_history_type) * (MAX_HISTORY_ITEMS - 1));

			memcpy (campaign_history, temp, sizeof (campaign_history_type) * (MAX_HISTORY_ITEMS - 1));

			num_campaign_history_items = MAX_HISTORY_ITEMS - 1;
		}

		campaign_history [num_campaign_history_items].page = page;

		campaign_history [num_campaign_history_items].index = index;

		#if DEBUG_MODULE

		debug_filtered_log ("CA_HIST: Push (%d) Page %d Index %d", num_campaign_history_items, page, index);

		#endif

		num_campaign_history_items ++;
	}
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void pop_campaign_history (ui_object *obj, void *arg)
{
	int
		i,
		page,
		index;

	ASSERT (!pop_flag);

	if (num_campaign_history_items > 0)
	{
		i = num_campaign_history_items - 1;

		page = campaign_history [i].page;

		index = campaign_history [i].index;

		#if DEBUG_MODULE

		debug_filtered_log ("CA_HIST: Popping to (%d) Page %d Index %d", i, page, index);

		#endif

		pop_flag = TRUE;

		display_campaign_page ((campaign_pages) page, index, FALSE);

		notify_campaign_screen (CAMPAIGN_SCREEN_FORCE_UPDATE, get_local_entity_safe_ptr (index));

		num_campaign_history_items = i;

		pop_flag = FALSE;
	}
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

static void remove_campaign_history (int item)
{
	int
		count;

	count = num_campaign_history_items;

	ASSERT ((item >= 0) && (item < count));

	count = count - 1;

	if (item > 0)
	{
		//
		// copy elements before removed item
		//

		memcpy (temp, campaign_history, sizeof (campaign_history_type) * item);
	}

	if (count > item)
	{
		//
		// copy elements after removed item
		//

		memcpy (&temp [item], &campaign_history [item + 1], sizeof (campaign_history_type) * (count - item));
	}

	//
	// switch lists
	//

	if (count > 0)
	{
		memcpy (campaign_history, temp, sizeof (campaign_history_type) * count);
	}

	num_campaign_history_items = count;
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

static void maintain_campaign_history (void)
{
	int
		loop,
		last,
		page;

	for (loop = 1; loop < num_campaign_history_items; loop ++)
	{
		last = loop - 1;

		if (campaign_history [loop].page == campaign_history [last].page)
		{
			if (campaign_history [loop].index == campaign_history [last].index)
			{
				remove_campaign_history (last);

				loop --;
			}
		}
	}

	//
	// Consider currently displayed page
	//

	last = num_campaign_history_items - 1;

	page = campaign_history [last].page;

	if (get_ui_object_drawable (campaign_page [page]))
	{
		if (get_ui_object_item_number (campaign_page [page]) == campaign_history [last].index)
		{
			remove_campaign_history (last);
		}
	}
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void remove_campaign_page_from_history (campaign_pages page)
{
	int
		loop;

	for (loop = 0; loop < num_campaign_history_items; loop ++)
	{
		if (campaign_history [loop].page == page)
		{
			remove_campaign_history (loop);

			loop --;
		}
	}

	maintain_campaign_history ();
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void define_campaign_page_back_button (ui_object *parent, ui_object **obj)
{
	float
		x1,
		y1,
		x2,
		y2;

	ASSERT (obj);

	ASSERT (parent);

	x1 = 0.0;
	y1 = 0.943;
	x2 = 0.051;
	y2 = 1.0;

	*obj = create_ui_object
		(
			UI_TYPE_BUTTON,
			UI_ATTR_PARENT (parent),
			UI_ATTR_VIRTUAL_POSITION (x1, y1),
			UI_ATTR_VIRTUAL_SIZE (x2 - x1, y2 - y1),
			UI_ATTR_FONT_TYPE (UI_FONT_ARIAL_16),
			UI_ATTR_HIGHLIGHTABLE (TRUE),
			UI_ATTR_FUNCTION (pop_campaign_history),
			UI_ATTR_TEXTURE_GRAPHIC (create_texture_graphic ("graphics\\ui\\cohokum\\map\\barrprp.psd", 0)),
			UI_ATTR_HIGHLIGHTED_TEXTURE_GRAPHIC (create_texture_graphic ("graphics\\ui\\cohokum\\map\\barrwht.psd", 0)),
			UI_ATTR_SELECTED_TEXTURE_GRAPHIC (create_texture_graphic ("graphics\\ui\\cohokum\\map\\barryel.psd", 0)),
			UI_ATTR_END
		);
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

static int response_to_campaign_item_destroyed (campaign_screen_messages message, entity *sender)
{
	int
		loop,
		index;

	ASSERT (sender);

	index = get_local_entity_safe_index (sender);

	for (loop = 0; loop < num_campaign_history_items; loop ++)
	{
		if (campaign_history [loop].index == index)
		{
			remove_campaign_history (loop);

			loop --;
		}
	}

	maintain_campaign_history ();

	return TRUE;
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void overload_campaign_history_message_responses (void)
{
	campaign_screen_message_targets
		target;

	target = CAMPAIGN_SCREEN_TARGET_HISTORY;

	campaign_screen_message_responses [target][CAMPAIGN_SCREEN_MISSION_COMPLETED]	=	response_to_campaign_item_destroyed;

	campaign_screen_message_responses [target][CAMPAIGN_SCREEN_MISSION_DESTROYED]	=	response_to_campaign_item_destroyed;

	campaign_screen_message_responses [target][CAMPAIGN_SCREEN_GROUP_DESTROYED]	=	response_to_campaign_item_destroyed;

	campaign_screen_message_responses [target][CAMPAIGN_SCREEN_BASE_DESTROYED]		=	response_to_campaign_item_destroyed;
}

