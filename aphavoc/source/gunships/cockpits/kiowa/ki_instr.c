float get_kiowa_virtual_cockpit_adi_slip_indicator_needle_value (void)
{
	float
		slip_needle_value;

	slip_needle_value = get_adi_slip_indicator_needle_value ();

	slip_needle_value *= rad (30.0) / 30.0;

	return (slip_needle_value);
}

