static void toggle_night_vision_system_event (event *ev)
{
	if (!comanche_damage.pnvs)
	{
		night_vision_system_active ^= 1;
	}
	else
	{
		play_client_server_cpg_message (get_gunship_entity (), 0.5, 1.0, SPEECH_CATEGORY_CPG_SYSTEMS, 1.0, SPEECH_CPG_PNVS_DAMAGED);
	}
}

