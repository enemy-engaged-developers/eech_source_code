static void activate_laser_event(event* ev)
{
	if (!laser_is_active() && !ka50_damage.laser_range_finder && get_local_entity_parent (get_gunship_entity (), LIST_TYPE_TARGET))
		set_laser_is_active(TRUE);
	else
		set_laser_is_active(FALSE);
}

