void update_apache_ground_radar (void)
{
	////////////////////////////////////////

	while (single_target_acquisition_system_steer_left_key)
	{
		float amount = -APACHE_GROUND_RADAR_SCAN_DATUM_ROTATE_STEP;
		rotate_radar_scan_datum(amount);
		single_target_acquisition_system_steer_left_key--;
	}

	while (single_target_acquisition_system_steer_left_fast_key)
	{
		fast_rotate_ground_radar_scan_datum_left ();

		single_target_acquisition_system_steer_left_fast_key--;
	}

	////////////////////////////////////////

	while (single_target_acquisition_system_steer_right_key)
	{
		float amount = APACHE_GROUND_RADAR_SCAN_DATUM_ROTATE_STEP;
		rotate_radar_scan_datum(amount);
		single_target_acquisition_system_steer_right_key--;
	}

	while (single_target_acquisition_system_steer_right_fast_key)
	{
		fast_rotate_ground_radar_scan_datum_right ();

		single_target_acquisition_system_steer_right_fast_key--;
	}

	////////////////////////////////////////

	while (single_target_acquisition_system_steer_up_key)
	{
		inc_ground_radar_scan_arc_size ();

		single_target_acquisition_system_steer_up_key--;
	}

	while (single_target_acquisition_system_steer_up_fast_key)
	{
		fast_inc_ground_radar_scan_arc_size ();

		single_target_acquisition_system_steer_up_fast_key--;
	}

	////////////////////////////////////////

	while (single_target_acquisition_system_steer_down_key)
	{
		dec_ground_radar_scan_arc_size ();

		single_target_acquisition_system_steer_down_key--;
	}

	while (single_target_acquisition_system_steer_down_fast_key)
	{
		fast_dec_ground_radar_scan_arc_size ();

		single_target_acquisition_system_steer_down_fast_key--;
	}

	////////////////////////////////////////

	while (single_target_acquisition_system_inc_range_key)
	{
		inc_ground_radar_scan_range ();

		single_target_acquisition_system_inc_range_key--;
	}

	while (single_target_acquisition_system_inc_range_fast_key)
	{
		fast_inc_ground_radar_scan_range ();

		single_target_acquisition_system_inc_range_fast_key--;
	}

	////////////////////////////////////////

	while (single_target_acquisition_system_dec_range_key)
	{
		dec_ground_radar_scan_range ();

		single_target_acquisition_system_dec_range_key--;
	}

	while (single_target_acquisition_system_dec_range_fast_key)
	{
		fast_dec_ground_radar_scan_range ();

		single_target_acquisition_system_dec_range_fast_key--;
	}

	////////////////////////////////////////

	while (single_target_acquisition_system_inc_target_priority_key)
	{
		inc_ground_radar_target_priority ();

		single_target_acquisition_system_inc_target_priority_key--;
	}

	while (single_target_acquisition_system_inc_target_priority_fast_key)
	{
		fast_inc_ground_radar_target_priority ();

		single_target_acquisition_system_inc_target_priority_fast_key--;
	}

	////////////////////////////////////////

	while (single_target_acquisition_system_dec_target_priority_key)
	{
		dec_ground_radar_target_priority ();

		single_target_acquisition_system_dec_target_priority_key--;
	}

	while (single_target_acquisition_system_dec_target_priority_fast_key)
	{
		fast_dec_ground_radar_target_priority ();

		single_target_acquisition_system_dec_target_priority_fast_key--;
	}

	////////////////////////////////////////

	while (single_target_acquisition_system_select_next_target_key)
	{
		select_next_ground_radar_target ();

		single_target_acquisition_system_select_next_target_key--;
	}

	////////////////////////////////////////

	while (single_target_acquisition_system_select_previous_target_key)
	{
		select_previous_ground_radar_target ();

		single_target_acquisition_system_select_previous_target_key--;
	}

// Jabberwock 031107 Designated targets

	while (single_target_acquisition_system_select_next_designated_key)
	{
		select_next_designated_ground_radar_target ();

		single_target_acquisition_system_select_next_designated_key--;
	}

	////////////////////////////////////////

	while (single_target_acquisition_system_select_previous_designated_key)
	{
		select_next_designated_ground_radar_target ();

		single_target_acquisition_system_select_previous_designated_key--;
	}
// Jabberwock 031107 ends
}

