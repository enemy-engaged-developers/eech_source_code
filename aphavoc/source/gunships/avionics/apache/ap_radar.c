static void inc_ground_radar_scan_arc_size (void)
{
	if (ground_radar.scan_arc_size == APACHE_RADAR_SCAN_ARC_SIZE_15)
	{
		ground_radar.scan_arc_size = APACHE_RADAR_SCAN_ARC_SIZE_30;
	}
	else if (ground_radar.scan_arc_size == APACHE_RADAR_SCAN_ARC_SIZE_30)
	{
		ground_radar.scan_arc_size = APACHE_RADAR_SCAN_ARC_SIZE_45;
	}
	else if (ground_radar.scan_arc_size == APACHE_RADAR_SCAN_ARC_SIZE_45)
	{
		ground_radar.scan_arc_size = APACHE_RADAR_SCAN_ARC_SIZE_60;
	}
	else if (ground_radar.scan_arc_size == APACHE_RADAR_SCAN_ARC_SIZE_60)
	{
		ground_radar.scan_arc_size = APACHE_RADAR_SCAN_ARC_SIZE_90;
	}

	if ((ground_radar.scan_datum - (ground_radar.scan_arc_size * 0.5)) < -APACHE_RADAR_SCAN_ARC_SIZE_45)
	{
		ground_radar.scan_datum = -APACHE_RADAR_SCAN_ARC_SIZE_45 + (ground_radar.scan_arc_size * 0.5);
	}
	else if ((ground_radar.scan_datum + (ground_radar.scan_arc_size * 0.5)) > APACHE_RADAR_SCAN_ARC_SIZE_45)
	{
		ground_radar.scan_datum = APACHE_RADAR_SCAN_ARC_SIZE_45 - (ground_radar.scan_arc_size * 0.5);
	}

	limit_radar_sweep (&ground_radar);
}

