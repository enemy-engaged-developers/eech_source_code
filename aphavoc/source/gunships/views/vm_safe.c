#include "project.h"

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#define VELOCITY (kilometres_per_hour_to_metres_per_second (200.0))

#define LEFT	(-1.0)
#define RIGHT	(1.0)
#define UP		(-1.0)
#define DOWN	(1.0)

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

static float
	dx,
	dz;

static vec3d
	position;

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void initialise_safe_view (void)
{
	dx = RIGHT;
	dz = UP;

	position.x = MIN_MAP_X + ((MAX_MAP_X - MIN_MAP_X) * frand1 ());
	position.z = MIN_MAP_Z + ((MAX_MAP_Z - MIN_MAP_Z) * frand1 ());

	ASSERT(point_inside_map_area(&position));

	position.y = get_3d_terrain_elevation (position.x, position.z) + 50.0;
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void deinitialise_safe_view (void)
{
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void update_safe_view (void)
{
	////////////////////////////////////////

	position.x += VELOCITY * dx * get_delta_time ();

	if ((position.x < MIN_MAP_X) && (dx == LEFT))
	{
		dx = RIGHT;
	}

	if ((position.x > MAX_MAP_X) && (dx == RIGHT))
	{
		dx = LEFT;
	}

	////////////////////////////////////////

	position.z += VELOCITY * dz * get_delta_time ();

	if ((position.z < MIN_MAP_Z) && (dz == UP))
	{
		dz = DOWN;
	}

	if ((position.z > MAX_MAP_Z) && (dz == DOWN))
	{
		dz = UP;
	}

	////////////////////////////////////////

	if (point_inside_map_area (&position))
	{
		position.y = get_3d_terrain_elevation (position.x, position.z) + 50.0;
	}
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void draw_safe_3d_view (void)
{
   //
   // get camera position and attitude
   //

	main_vp.position = position;

	get_3d_transformation_matrix (main_vp.attitude, atan2 (dx, dz), rad (-10.0), 0.0);

   //
   // draw 3D scene
   //

	set_main_3d_full_screen_params (DISPLAY_3D_TINT_CLEAR, DISPLAY_3D_LIGHT_LEVEL_HIGH, DISPLAY_3D_NOISE_LEVEL_NONE, 1.0);

	draw_main_3d_scene (&main_vp);
}

