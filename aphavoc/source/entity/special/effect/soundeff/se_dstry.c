void overload_sound_effect_destroy_functions (void)
{
	fn_destroy_local_entity						[ENTITY_TYPE_SOUND_EFFECT]								= destroy_local;
	fn_destroy_client_server_entity			[ENTITY_TYPE_SOUND_EFFECT][COMMS_MODEL_SERVER]	= destroy_server;
	fn_destroy_client_server_entity			[ENTITY_TYPE_SOUND_EFFECT][COMMS_MODEL_CLIENT]	= destroy_client;

	fn_destroy_local_entity_family			[ENTITY_TYPE_SOUND_EFFECT]								= destroy_local;
	fn_destroy_client_server_entity_family	[ENTITY_TYPE_SOUND_EFFECT][COMMS_MODEL_SERVER]	= destroy_server;
	fn_destroy_client_server_entity_family	[ENTITY_TYPE_SOUND_EFFECT][COMMS_MODEL_CLIENT]	= destroy_client;

	fn_kill_local_entity							[ENTITY_TYPE_SOUND_EFFECT]								= kill_local;
	fn_kill_client_server_entity				[ENTITY_TYPE_SOUND_EFFECT][COMMS_MODEL_SERVER]	= kill_server;
	fn_kill_client_server_entity				[ENTITY_TYPE_SOUND_EFFECT][COMMS_MODEL_CLIENT]	= kill_client;
}

