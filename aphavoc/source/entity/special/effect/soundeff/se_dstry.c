static void destroy_local (entity *en)
{
	sound_effect
		*raw;

	////////////////////////////////////////
	//
	// PRE-AMBLE
	//
	////////////////////////////////////////

	#if DEBUG_MODULE

	debug_log_entity_args (ENTITY_DEBUG_LOCAL, ENTITY_DEBUG_DESTROY, en);

	#endif

	raw = (sound_effect *) get_local_entity_data (en);

	ASSERT (raw->effect_index);

	#if DEBUG_MODULE

	debug_log ("SE_DSTRY : Destroyed Sound effect %s ( index = %d, parent = %d, lifetime = %f (%d))",
						application_sound_effects [raw->effect_index [0]].name,
						get_local_entity_index (en),
						get_local_entity_index (get_local_entity_parent (en, LIST_TYPE_SPECIAL_EFFECT)),
						raw->effect_lifetime,
						raw->valid_sound_effect);

	#endif
	
	////////////////////////////////////////
	//
	// VALIDATE
	//
	////////////////////////////////////////

	////////////////////////////////////////
	//
	// DESTROY COMPONENTS
	//
	////////////////////////////////////////

	if (raw->sound_effect_data)
	{

		destroy_system_sound_effect (raw->sound_effect_data);
	}

	////////////////////////////////////////
	//
	// UNLINK FROM SYSTEM
	//
	////////////////////////////////////////

	delete_local_entity_from_parents_child_list (en, LIST_TYPE_UPDATE);

	delete_local_entity_from_parents_child_list (en, LIST_TYPE_SPECIAL_EFFECT);

	////////////////////////////////////////
	//
	// FREE ENTITY DATA
	//
	////////////////////////////////////////

	free_mem (raw->effect_index);

	free_mem (raw);

	set_free_entity (en);
}

