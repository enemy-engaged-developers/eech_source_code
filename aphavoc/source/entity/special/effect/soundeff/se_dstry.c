/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

static void kill_local (entity *en)
{
	
	sound_effect
		*raw;

	#if DEBUG_MODULE

	debug_log_entity_args (ENTITY_DEBUG_LOCAL, ENTITY_DEBUG_KILL, en);

	#endif

	raw = (sound_effect *) get_local_entity_data (en);

	#if DEBUG_MODULE
	
	debug_log ("SE_DSTRY : kill effect %s (%d), parent %s (%d), looping %d, valid %d",
								application_sound_effects [raw->effect_index [0]].name,
								get_local_entity_index (en),
								get_local_entity_type_name (get_local_entity_parent (en, LIST_TYPE_SPECIAL_EFFECT)),
								get_local_entity_index (get_local_entity_parent (en, LIST_TYPE_SPECIAL_EFFECT)),
								raw->looping,
								raw->valid_sound_effect);

	#endif

	if ((raw->eff.sub_type == ENTITY_SUB_TYPE_EFFECT_SOUND_ROTOR_WIND_UP1) ||
		(raw->eff.sub_type == ENTITY_SUB_TYPE_EFFECT_SOUND_ROTOR_WIND_UP2) ||
		(raw->eff.sub_type == ENTITY_SUB_TYPE_EFFECT_SOUND_ROTOR_WIND_DOWN1) ||
		(raw->eff.sub_type == ENTITY_SUB_TYPE_EFFECT_SOUND_ROTOR_WIND_DOWN2))
	{
		//
		// don't destroy rotor sounds, just stop playing and reset timers
		//

		raw->valid_sound_effect = FALSE;

		raw->valid_effect_lifetime = 0.0;
	}
	else
	{
		//
		// destroy sound effect
		//
	
		destroy_local_entity (en);
	}
}

