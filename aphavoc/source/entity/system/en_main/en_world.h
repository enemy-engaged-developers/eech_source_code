struct WORLD_MAP_DATA
{
	int
		sector_side_length,			// must be 2^n value
		sector_side_length_mask,
		num_map_x_sectors,
		num_map_z_sectors,
		num_map_sectors,
		min_map_x_sector,
		min_map_z_sector,
		max_map_x_sector,
		max_map_z_sector;

	float
		min_map_x,
		min_map_y,
		min_map_z,
		mid_map_x,
		mid_map_y,
		mid_map_z,
		max_map_x,
		max_map_y,
		max_map_z;
};

typedef struct WORLD_MAP_DATA world_map_data;

