/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void set_mono_font_rel_position (const float x_rel, const float y_rel)
{
	mono_font_x_position += x_rel;
	mono_font_y_position += y_rel;
}

