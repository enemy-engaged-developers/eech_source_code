/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void print_mono_font_char (const char c)
{
	const char
		*sprite_ptr;

	//ASSERT (c >= 0 && c < NUM_CHARACTERS);
	ASSERT (c > 0 && c < NUM_CHARACTERS);

	sprite_ptr = mono_font_cells + (c * mono_font_size);

	draw_mono_sprite (sprite_ptr, mono_font_x_position, mono_font_y_position, mono_font_colour);

	mono_font_x_position += mono_font_width + 1.0;
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

float get_mono_font_string_width (const char *s)
{
	float
		length,
		width;

	length = (float) strlen (s);

	//
	// add an extra pixel spacing per character (except the last)
	//

	width = (length * (mono_font_width + 1.0)) - 1.0;

	return (width);
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void print_mono_font_string (const char *s)
{
	while (*s)
	{
		print_mono_font_char (*s++);
	}
}

