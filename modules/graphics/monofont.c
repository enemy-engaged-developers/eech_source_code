static mono_font_types
	mono_font_type = MONO_FONT_TYPE_5X9;

static rgb_colour
	mono_font_colour = {255, 255, 255, 255};

static const char
	*mono_font_cells = ( const char * ) mono_font_5x9_cells;

static float
	mono_font_x_position = 0.0,
	mono_font_y_position = 0.0,
	mono_font_width = CELL_WIDTH_5X9,
	mono_font_height = CELL_HEIGHT_5X9;

static int
	mono_font_size = CELL_SIZE_5X9;

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void set_mono_font_type (const mono_font_types type)
{
	ASSERT ((type >= 0) && (type < NUM_MONO_FONT_TYPES));

	mono_font_type = type;

	mono_font_cells = mono_font_cells_table[type];

	mono_font_width = mono_font_width_table[type];

	mono_font_height = mono_font_height_table[type];

	mono_font_size = mono_font_size_table[type];
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

mono_font_types get_mono_font_type (void)
{
	return (mono_font_type);
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void set_mono_font_position (const float x, const float y)
{
	mono_font_x_position = x;
	mono_font_y_position = y;
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void get_mono_font_position (float *x, float *y)
{
	*x = mono_font_x_position;
	*y = mono_font_y_position;
}

