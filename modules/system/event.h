//
// Mouse events start at 256, ie after all of the DIK events.
//

enum DEVICE_EVENTS
{

	MOUSE_LEFT_BUTTON 		= 256,
	MOUSE_RIGHT_BUTTON,
	MOUSE_MOVE,
	MOUSE_MOVE_UP,
	MOUSE_MOVE_DOWN,
	MOUSE_MOVE_LEFT,
	MOUSE_MOVE_RIGHT,
	JOYSTICK_MOVE,
	JOYSTICK_BUTTON,
	NUM_DEVICE_EVENTS
};

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

enum BUTTON_STATES
{

	BUTTON_STATE_INVALID,
	BUTTON_STATE_UP,
	BUTTON_STATE_DOWN,
	BUTTON_STATE_EITHER,
};

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

enum KEY_STATES
{

	KEY_STATE_INVALID,
	KEY_STATE_UP,
	KEY_STATE_DOWN,
	KEY_STATE_EITHER,
};

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

enum EVENT_TYPES
{

   EVENT_TYPE_INVALID,
   EVENT_TYPE_KEY,
   EVENT_TYPE_MOUSE_BUTTON,
   EVENT_TYPE_MOUSE_MOVE,
   EVENT_TYPE_JOYSTICK_BUTTON,
   EVENT_TYPE_JOYSTICK_MOVE,
   EVENT_TYPE_TIMER,
};

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

enum MODIFIER_TYPES
{

   MODIFIER_NONE,
   MODIFIER_LEFT_SHIFT,
   MODIFIER_LEFT_CONTROL,
   MODIFIER_LEFT_ALT,
   MODIFIER_RIGHT_SHIFT,
   MODIFIER_RIGHT_CONTROL,
   MODIFIER_RIGHT_ALT,
	MODIFIER_MOUSE_LEFT_BUTTON,
	MODIFIER_MOUSE_RIGHT_BUTTON,
   NUM_MODIFIER_TYPES
};

extern char
	*button_state_names [],
	*modifier_type_names [];

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct EVENT
{

   enum EVENT_TYPES
      type;

	unsigned char
		modifier,
		device_index,
		key;

	unsigned short int
		state,
		button;

	short int
		dx,
		dy,
		dz;
};

typedef struct EVENT event;

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct REGISTERED_EVENT_TYPE
{

	unsigned char
      in_use,
      current_state,
      notify_state;

   void
      // ((*function) (void *arg));
      ((*function) (event *arg));
};

typedef struct REGISTERED_EVENT_TYPE registered_event_type;

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

extern registered_event_type
   registered_events [MAX_NUMBER_REGISTERED_EVENTS][NUM_MODIFIER_TYPES];

extern int
   current_modifier_states;

