#define MAX_NUMBER_JOYSTICK_BUTTONS		32

#define MAX_NUMBER_REGISTERED_EVENTS   (NUM_DEVICE_EVENTS + MAX_NUMBER_JOYSTICK_BUTTONS)

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
