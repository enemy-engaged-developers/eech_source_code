enum DIRECTORY_FILE_TYPES get_directory_file_type ( directory_file_list *list )
{

	char fullpath[ strlen(list->path) + 1 + strlen(list->entry->d_name) + 1 ];

	struct stat buf;

	sprintf(fullpath, "%s/%s", list->path, list->entry->d_name);

	stat(fullpath, &buf);

	if ( S_ISDIR(buf.st_mode) )
	{

		return ( DIRECTORY_FILE_TYPE_DIRECTORY );
	}
	else
	{

		return ( DIRECTORY_FILE_TYPE_FILE );
	}
}

