int file_extension ( char *filename, char *extension )
{

	char
		*ptr,
		*string;

	//
	// Search for the "."
	//

	ptr = filename;

	while ( ( *ptr != '\0' ) && ( *ptr != '.' ) )
	{

		ptr++;
	}

	if ( *ptr == '.' )
	{

		ptr++;
	}

	string = ptr;

	while ( *string != '\0' )
	{

		*string = toupper ( *string );

		string++;
	}

	string = extension;

	while ( *string != '\0' )
	{

		*string = toupper ( *string );

		string++;
	}

	if ( strcmp ( ptr, extension ) == 0 )
	{

		return ( TRUE );
	}
	else
	{

		return ( FALSE );
	}
}

