time_t get_directory_file_last_write_time ( directory_file_list *list )
{

	char fullpath[ strlen(list->path) + 1 + strlen(list->entry->d_name) + 1 ];

	struct stat buf;

	sprintf(fullpath, "%s/%s", list->path, list->entry->d_name);

	stat(fullpath, &buf);

	return ( buf.st_mtime );
}

