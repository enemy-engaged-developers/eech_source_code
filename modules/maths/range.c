float get_approx_2d_range (const vec3d *v1, const vec3d *v2)
{
	float
		dx,
		dz,
		range;

	ASSERT (v1);
	ASSERT (v2);

	dx = fabs (v1->x - v2->x);
	dz = fabs (v1->z - v2->z);

	if (dx > dz)
	{
		range = ((dx * 4.0) + dz) * (1.0 / 4.0);
	}
	else
	{
		range = ((dz * 4.0) + dx) * (1.0 / 4.0);
	}

	return (range);
}

