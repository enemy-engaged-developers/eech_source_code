float get_approx_3d_range (const vec3d *v1, const vec3d *v2)
{
	float
		dx,
		dy,
		dz,
		tmp,
		range;

	ASSERT (v1);
	ASSERT (v2);

	dx = fabs (v1->x - v2->x);
	dy = fabs (v1->y - v2->y);
	dz = fabs (v1->z - v2->z);

	//
	// approx 3D range = max + ((med + min) / 4) (only need to find max value)
	//

	if (dx < dy)
	{
		tmp = dx; dx = dy; dy = tmp;
	}

	if (dx < dz)
	{
		tmp = dx; dx = dz; dz = tmp;
	}

	range = dx + ((dy + dz) * (1.0 / 4.0));

	return (range);
}

