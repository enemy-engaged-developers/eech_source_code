float get_2d_range (const vec3d *v1, const vec3d *v2)
{
	float
		dx,
		dz,
		range;

	ASSERT (v1);
	ASSERT (v2);

	dx = v1->x - v2->x;
	dz = v1->z - v2->z;

	range = sqrt ((dx * dx) + (dz * dz));

	return (range);
}

