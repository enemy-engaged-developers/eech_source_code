float get_3d_range (const vec3d *v1, const vec3d *v2)
{
	float
		dx,
		dy,
		dz,
		range;

	ASSERT (v1);
	ASSERT (v2);

	dx = v1->x - v2->x;
	dy = v1->y - v2->y;
	dz = v1->z - v2->z;

	range = sqrt ((dx * dx) + (dy * dy) + (dz * dz));

	return (range);
}

