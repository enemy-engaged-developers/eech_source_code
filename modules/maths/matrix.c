void get_reflection_matrix ( matrix3x3 m, const vec3d *v )
{

	float
		n1_n1,
		n1_n2,
		n1_n3,
		n2_n3,
		n2_n2,
		n3_n3;

	n1_n1 = ( v->x * v->x );
	n1_n2 = ( v->x * v->y );
	n1_n3 = ( v->x * v->z );
	n2_n3 = ( v->y * v->z );
	n2_n2 = ( v->y * v->y );
	n3_n3 = ( v->z * v->z );

	m[0][0] = n1_n1 + n1_n1 - 1.0;
	m[0][1] = n1_n2 + n1_n2;
	m[0][2] = n1_n3 + n1_n3;
	m[1][0] = n1_n2 + n1_n2;
	m[1][1] = n2_n2 + n2_n2 - 1.0;
	m[1][2] = n2_n3 + n2_n3;
	m[2][0] = n1_n3 + n1_n3;
	m[2][1] = n2_n3 + n2_n3;
	m[2][2] = n3_n3 + n3_n3 - 1.0;
}

