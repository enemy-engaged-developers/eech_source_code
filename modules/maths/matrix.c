void get_4d_projection_matrix ( matrix4x4 m, float near_plane, float far_plane, float fov_width, float fov_height )
{

	float
		h,
		w,
		Q;

	w = ( float ) ( 1.0 / ( tan ( fov_width * 0.5 ) ) );
	h = ( float ) ( 1.0 / ( tan ( fov_height * 0.5 ) ) );
	Q = far_plane / ( far_plane - near_plane );

	memset ( m, 0, sizeof ( matrix4x4 ) );

	m[0][0] = w;
	m[1][1] = h;
	m[2][2] = Q;
	m[3][2] = -Q*near_plane;
	m[2][3] = 1;
}

