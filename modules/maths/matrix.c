void multiply_matrix3x3_vec3d ( vec3d *result, const matrix3x3 m, const vec3d *v )
{

	vec3d
		temp;

	temp.x = v->x * m[0][0];
	temp.x += v->y * m[1][0];
	temp.x += v->z * m[2][0];

	temp.y = v->x * m[0][1];
	temp.y += v->y * m[1][1];
	temp.y += v->z * m[2][1];

	temp.z = v->x * m[0][2];
	temp.z += v->y * m[1][2];
	temp.z += v->z * m[2][2];

	result->x = temp.x;
	result->y = temp.y;
	result->z = temp.z;
}

