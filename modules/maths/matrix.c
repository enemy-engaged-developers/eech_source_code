{

	float
		sinh,
		cosh;

	sinh = sin ( heading );
	cosh = cos ( heading );

	m[0][0] = cosh;
	m[0][1] = 0.0;
	m[0][2] = -sinh;
	m[1][0] = 0.0;
	m[1][1] = 1.0;
	m[1][2] = 0.0;
	m[2][0] = sinh;
	m[2][1] = 0.0;
	m[2][2] = cosh;
}

