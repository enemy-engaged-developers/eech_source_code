void get_3d_transformation_heading_pitch_matrix ( matrix3x3 m, float heading, float pitch )
{

	float
		sinh,
		cosh,
		sinp,
		cosp;

	sinh = sin ( heading );
	cosh = cos ( heading );

	sinp = sin ( pitch );
	cosp = cos ( pitch );

	m[0][0] = cosh;
	m[0][1] = 0;
	m[0][2] = -sinh;
	m[1][0] = -( sinh * sinp );
	m[1][1] = cosp;
	m[1][2] = -( cosh * sinp );
	m[2][0] = cosp * sinh;
	m[2][1] = sinp;
	m[2][2] = cosp * cosh;
}

