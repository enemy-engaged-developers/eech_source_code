void get_3d_transformation_double_matrix ( double_matrix3x3 m, double heading, double pitch, double roll )
{

	double
		sinh,
		cosh,
		sinp,
		cosp,
		sinr,
		cosr,
		sinrsinh,
		sinrcosh,
		cosrsinh,
		cosrcosh;

	sinh = sin ( heading );
	cosh = cos ( heading );

	sinp = sin ( pitch );
	cosp = cos ( pitch );

	sinr = sin ( roll );
	cosr = cos ( roll );

	sinrsinh = sinr * sinh;

	sinrcosh = sinr * cosh;

	cosrsinh = cosr * sinh;

	cosrcosh = cosr * cosh;

	m [ 0 ] [ 0 ] = cosrcosh + ( sinrsinh * sinp );

	m [ 0 ] [ 1 ] = -sinr * cosp;

	m [ 0 ] [ 2 ] = ( sinrcosh * sinp ) - cosrsinh;

	m [ 1 ] [ 0 ] = sinrcosh - ( cosrsinh * sinp );

	m [ 1 ] [ 1 ] = cosr * cosp;

	m [ 1 ] [ 2 ] = -sinrsinh - ( cosrcosh * sinp );

	m [ 2 ] [ 0 ] = cosp * sinh;

	m [ 2 ] [ 1 ] = sinp;

	m [ 2 ] [ 2 ] = cosp * cosh;
}

