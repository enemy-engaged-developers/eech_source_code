void get_4d_transformation_matrix ( matrix4x4 m, float heading, float pitch, float roll, const vec3d *position )
{

	float
		sinh,
		cosh,
		sinp,
		cosp,
		sinr,
		cosr,
		sinrsinh,
		sinrcosh,
		cosrsinh,
		cosrcosh;

	sinh = sin ( heading );
	cosh = cos ( heading );

	sinp = sin ( pitch );
	cosp = cos ( pitch );

	sinr = sin ( roll );
	cosr = cos ( roll );

	sinrsinh = sinr * sinh;

	sinrcosh = sinr * cosh;

	cosrsinh = cosr * sinh;

	cosrcosh = cosr * cosh;

	m[0][0] = cosrcosh + ( sinrsinh * sinp );
	m[0][1] = -sinr * cosp;
	m[0][2] = ( sinrcosh * sinp ) - cosrsinh;
	m[0][3] = 0;
	m[1][0] = sinrcosh - ( cosrsinh * sinp );
	m[1][1] = cosr * cosp;
	m[1][2] = -sinrsinh - ( cosrcosh * sinp );
	m[1][3] = 0;
	m[2][0] = cosp * sinh;
	m[2][1] = sinp;
	m[2][2] = cosp * cosh;
	m[2][3] = 0;
	m[3][0] = position->x;
	m[3][1] = position->y;
	m[3][2] = position->z;
	m[3][3] = 1;
}

