// optimised for 2D graphics:
//
//	           (a[0][0] a[0][1] 0)   (b[0][0] b[0][1] 0)
// (result) = (a[1][0] a[1][1] 0) * (b[1][0] b[1][1] 0)
//            (a[2][0] a[2][1] 1)   (b[2][0] b[2][1] 1)

void multiply_matrix3x3_matrix3x3_2d (matrix3x3 result, const matrix3x3 a, const matrix3x3 b)
{
	result[0][0] = (a[0][0] * b[0][0]) + (a[0][1] * b[1][0]);
	result[0][1] = (a[0][0] * b[0][1]) + (a[0][1] * b[1][1]);
	result[0][2] = 0.0;

	result[1][0] = (a[1][0] * b[0][0]) + (a[1][1] * b[1][0]);
	result[1][1] = (a[1][0] * b[0][1]) + (a[1][1] * b[1][1]);
	result[1][2] = 0.0;

	result[2][0] = (a[2][0] * b[0][0]) + (a[2][1] * b[1][0]) + b[2][0];
	result[2][1] = (a[2][0] * b[0][1]) + (a[2][1] * b[1][1]) + b[2][1];
	result[2][2] = 1.0;
}

