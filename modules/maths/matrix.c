	result[0][2] = ( ( a[0][2] * b[0][2] ) + ( a[1][2] * b[1][2] ) + ( a[2][2] * b[2][2] ) );

	result[1][0] = ( ( a[0][0] * b[0][0] ) + ( a[1][0] * b[1][0] ) + ( a[2][0] * b[2][0] ) );
	result[1][1] = ( ( a[0][1] * b[0][1] ) + ( a[1][1] * b[1][1] ) + ( a[2][1] * b[2][1] ) );
	result[1][2] = ( ( a[0][2] * b[0][2] ) + ( a[1][2] * b[1][2] ) + ( a[2][2] * b[2][2] ) );

	result[2][0] = ( ( a[0][0] * b[0][0] ) + ( a[1][0] * b[1][0] ) + ( a[2][0] * b[2][0] ) );
	result[2][1] = ( ( a[0][1] * b[0][1] ) + ( a[1][1] * b[1][1] ) + ( a[2][1] * b[2][1] ) );
