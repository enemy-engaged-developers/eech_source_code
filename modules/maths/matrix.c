void get_3d_transformation_matrix_fast ( matrix3x3 m, float heading, float pitch, float roll )
{

	float
		sinh,
		cosh,
		sinp,
		cosp,
		sinr,
		cosr,
		sinrsinh,
		sinrcosh,
		cosrsinh,
		cosrcosh;

	sine_cosine ( heading, &sinh, &cosh );

	sine_cosine ( pitch, &sinp, &cosp );

	sine_cosine ( roll, &sinr, &cosr );

	sinrsinh = sinr * sinh;

	sinrcosh = sinr * cosh;

	cosrsinh = cosr * sinh;

	cosrcosh = cosr * cosh;

	m [ 0 ] [ 0 ] = cosrcosh + ( sinrsinh * sinp );

	m [ 0 ] [ 1 ] = -sinr * cosp;

	m [ 0 ] [ 2 ] = ( sinrcosh * sinp ) - cosrsinh;

	m [ 1 ] [ 0 ] = sinrcosh - ( cosrsinh * sinp );

	m [ 1 ] [ 1 ] = cosr * cosp;

	m [ 1 ] [ 2 ] = -sinrsinh - ( cosrcosh * sinp );

	m [ 2 ] [ 0 ] = cosp * sinh;

	m [ 2 ] [ 1 ] = sinp;

	m [ 2 ] [ 2 ] = cosp * cosh;
}

