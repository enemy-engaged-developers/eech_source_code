void get_arbitrary_rotation_matrix ( matrix3x3 m, const vec3d *v, float angle )
{

	float
		cosine,
		sine,

		n1_sin,
		n2_sin,
		n3_sin,

		n1_n1,
		n1_n2,
		n1_n3,
		n2_n3,

		n2_n2,
		n3_n3,

		n1_n2_cos,
		n1_n3_cos,
		n2_n3_cos,

		one_minus_n1_n1_cos,
		one_minus_n2_n2_cos,
		one_minus_n3_n3_cos;

	cosine = cos ( angle );

	sine = sin ( angle );

	n1_sin = ( v->x * sine );
	n2_sin = ( v->y * sine );
	n3_sin = ( v->z * sine );

	n1_n1 = ( v->x * v->x );
	n1_n2 = ( v->x * v->y );
	n1_n3 = ( v->x * v->z );
	n2_n3 = ( v->y * v->z );
	n2_n2 = ( v->y * v->y );
	n3_n3 = ( v->z * v->z );

	n1_n2_cos = ( n1_n2 * cosine );
	n1_n3_cos = ( n1_n3 * cosine );
	n2_n3_cos = ( n2_n3 * cosine );

	one_minus_n1_n1_cos = ( ( 1.0 - n1_n1 ) * cosine );
	one_minus_n2_n2_cos = ( ( 1.0 - n2_n2 ) * cosine );
	one_minus_n3_n3_cos = ( ( 1.0 - n3_n3 ) * cosine );

	m[0][0] = n1_n1 + one_minus_n1_n1_cos;
	m[0][1] = n1_n2 - n1_n2_cos - n3_sin;
	m[0][2] = n1_n3 - n1_n3_cos + n2_sin;
	m[1][0] = n1_n2 - n1_n2_cos + n3_sin;
	m[1][1] = n2_n2 + one_minus_n2_n2_cos;
	m[1][2] = n2_n3 - n2_n3_cos - n1_sin;
	m[2][0] = n1_n3 - n1_n3_cos - n2_sin;
	m[2][1] = n2_n3 - n2_n3_cos + n1_sin;
	m[2][2] = n3_n3 + one_minus_n3_n3_cos;
}

