void multiply_matrix4x4_vec4d ( vec4d *result, const matrix4x4 m, const vec4d *v )
{

	vec4d
		temp;

	temp.x = v->x * m[0][0];
	temp.x += v->y * m[1][0];
	temp.x += v->z * m[2][0];
	temp.x += v->a * m[3][0];

	temp.y = v->x * m[0][1];
	temp.y += v->y * m[1][1];
	temp.y += v->z * m[2][1];
	temp.y += v->a * m[3][1];

	temp.z = v->x * m[0][2];
	temp.z += v->y * m[1][2];
	temp.z += v->z * m[2][2];
	temp.z += v->a * m[3][2];

	temp.a = v->x * m[0][3];
	temp.a += v->y * m[1][3];
	temp.a += v->z * m[2][3];
	temp.a += v->a * m[3][3];

	result->x = temp.x;
	result->y = temp.y;
	result->z = temp.z;
	result->a = temp.a;
}

