// Kramer's Rule

void get_inverse_matrix (matrix3x3 result, const matrix3x3 matrix)
{

	float
		det;

	matrix3x3
		cofactors = {0};

	// form cofactors, with sign adjustment.

	// top row
	cofactors [0][0] = ((matrix [1][1] * matrix [2][2]) - (matrix [1][2] * matrix [2][1]));
	cofactors [1][0] = ((matrix [0][1] * matrix [2][2]) - (matrix [0][2] * matrix [2][1]));
	cofactors [2][0] = ((matrix [0][1] * matrix [1][2]) - (matrix [0][2] * matrix [1][1]));

	// mid row
	cofactors [0][1] = ((matrix [1][0] * matrix [2][2]) - (matrix [1][2] * matrix [2][0]));
	cofactors [1][1] = ((matrix [0][0] * matrix [2][2]) - (matrix [0][2] * matrix [2][0]));
	cofactors [2][1] = ((matrix [0][0] * matrix [1][2]) - (matrix [0][2] * matrix [1][0]));

	// bottom row
	cofactors [0][2] = ((matrix [1][0] * matrix [2][1]) - (matrix [1][1] * matrix [2][0]));
	cofactors [1][2] = ((matrix [0][0] * matrix [2][1]) - (matrix [0][1] * matrix [2][0]));
	cofactors [2][2] = ((matrix [0][0] * matrix [1][1]) - (matrix [0][1] * matrix [1][0]));

	// determinant

	det = (matrix [0][0] * cofactors [0][0]) -
			(matrix [1][0] * cofactors [1][0]) +
			(matrix [2][0] * cofactors [2][0]);

	// divide transposed cofactors by det

	// first column
	result [0][0] = +cofactors [0][0] / det;
	result [0][1] = -cofactors [1][0] / det;
	result [0][2] = +cofactors [2][0] / det;

	// second column
	result [1][0] = -cofactors [0][1] / det;
	result [1][1] = +cofactors [1][1] / det;
	result [1][2] = -cofactors [2][1] / det;

	// third column
	result [2][0] = +cofactors [0][2] / det;
	result [2][1] = -cofactors [1][2] / det;
	result [2][2] = +cofactors [2][2] / det;
}

