void ui_object_manager (void)
{

	ASSERT (get_ui_installed ());

	ASSERT (root_object);

	push_ui_screen (root_object);

   while (!exit_ui)
   {

      process_events ();

		//
		// Render to back screen
		//

		ui_repaint ();

		Sleep (0);

		set_ui_redraw (FALSE);

		if (ui_user_function)
		{

			ui_user_function ();
		}
   }

	ui_repaint ();
}

