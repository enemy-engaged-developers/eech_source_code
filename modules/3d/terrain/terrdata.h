#define TERRAIN_3D_SECTOR_SIDE_LENGTH_ADJUSTMENT 2048

//#define TERRAIN_3D_SECTOR_X_MAX 128

//#define TERRAIN_3D_SECTOR_Z_MAX 128

#define TERRAIN_3D_MIN_MAP_X	(0.0)
#define TERRAIN_3D_MIN_MAP_Z	(0.0)
