struct OBJECT_3D_SPRITE
{

	vec3d
		position;

	union
	{
	
		struct
		{
		
			unsigned char
				blue,
				green,
				red,
				alpha;
		};
	
		unsigned int
			colour;
	};

	float
		radius;

	float
		roll;

	int
		additive;

	struct SCREEN
		*texture;
};

typedef struct OBJECT_3D_SPRITE object_3d_sprite;

