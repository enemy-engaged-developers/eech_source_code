enum SAMPLE_STATES dsound_get_sound_buffer_status ( LPDIRECTSOUNDBUFFER buffer )
{

	HRESULT
		dsrval;

	DWORD
		status;

	ASSERT ( dsound );
	ASSERT ( buffer );

	dsrval = IDirectSoundBuffer_GetStatus ( buffer, &status );
	
	if ( dsrval != DS_OK )
	{

		debug_log ( "Unable to get buffer status: %s", get_dsound_error_message ( dsrval ) );
	}

	if ( ( status & DSBSTATUS_PLAYING ) || ( status & DSBSTATUS_LOOPING ) )
	{

		return ( SAMPLE_STATE_PLAYING );
	}
	else
	{

		return ( SAMPLE_STATE_FINISHED );
	}
}

