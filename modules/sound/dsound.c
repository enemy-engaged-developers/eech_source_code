void dsound_play_sound_buffer ( LPDIRECTSOUNDBUFFER buffer, int loop )
{

	HRESULT
		dsrval;

	ASSERT ( buffer );
	ASSERT ( dsound );

	if ( loop )
	{

		dsrval = IDirectSoundBuffer_Play ( buffer, 0, 0, DSBPLAY_LOOPING );

		if ( dsrval != DS_OK )
		{

			debug_log ( "Unable to play sound buffer looped: %s", get_dsound_error_message ( dsrval ) );
		}
	}
	else
	{

		dsrval = IDirectSoundBuffer_Play ( buffer, 0, 0, 0 );

		if ( dsrval != DS_OK )
		{

			debug_log ( "Unable to play sound buffer: %s", get_dsound_error_message ( dsrval ) );
		}
	}
}

