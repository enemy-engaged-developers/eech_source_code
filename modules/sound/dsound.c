unsigned char * dsound_lock_sound_buffer ( LPDIRECTSOUNDBUFFER buffer, int position, int size )
{

	unsigned	 char
		*data;

	DWORD
		length;

	HRESULT
		dsrval;

	ASSERT ( buffer );
	ASSERT ( size );
	ASSERT ( dsound );

	dsrval = IDirectSoundBuffer_Lock ( buffer, position, size, ( LPVOID * ) &data, &length, NULL, NULL, 0 );

	if ( dsrval != DS_OK )
	{

		debug_log ( "Unable to lock sound buffer: %s", get_dsound_error_message ( dsrval ) );

		return ( NULL );
	}
	else
	{

		return ( data );
	}
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

BOOL dsound_unlock_sound_buffer ( LPDIRECTSOUNDBUFFER buffer, unsigned char *data, int length )
{

	HRESULT
		dsrval;

	ASSERT ( dsound );
	ASSERT ( buffer );
	ASSERT ( data );
	ASSERT ( length );

	dsrval = IDirectSoundBuffer_Unlock ( buffer, ( LPVOID ) data, length, ( LPVOID ) NULL, 0 );

	if ( dsrval != DS_OK )
	{

		debug_log ( "Unable to unlock sound buffer: %s", get_dsound_error_message ( dsrval ) );

		return ( FALSE );
	}
	
	return ( TRUE );
}

